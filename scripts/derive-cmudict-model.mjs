#!/usr/bin/env node
import { execFileSync } from "node:child_process"
import { mkdtemp, mkdir, readFile, rm, writeFile } from "node:fs/promises"
import { tmpdir } from "node:os"
import { dirname, join, resolve } from "node:path"

const CMUDICT_SOURCE_URL = "https://raw.githubusercontent.com/cmusphinx/cmudict/master/cmudict.dict"
const WORDNET_SOURCE_URL = "https://wordnetcode.princeton.edu/3.0/WNdb-3.0.tar.gz"
const CONTEXT_SIZE = 4
const CMUDICT_OUTPUT_PATH = resolve("bin/cmudict-model.ts")
const WORDNET_OUTPUT_PATH = resolve("bin/wordnet-definitions.ts")

const definitionOnly = (gloss) =>
  gloss
    .split(";")
    .map((segment) => segment.trim())
    .filter(Boolean)
    .filter((segment) => !segment.startsWith('"'))
    .join("; ")

const cmudictResponse = await fetch(CMUDICT_SOURCE_URL)

if (!cmudictResponse.ok) {
  throw new Error(`Failed to download CMUdict from ${CMUDICT_SOURCE_URL}`)
}

const rawCmudict = await cmudictResponse.text()
const cmudictWords = new Set()

for (const line of rawCmudict.split(/\r?\n/)) {
  if (!line || line.startsWith(";;;")) {
    continue
  }

  const match = line.match(/^([^\s]+)\s+/)

  if (!match) {
    continue
  }

  const word = match[1].replace(/\(\d+\)$/, "").toLowerCase()

  if (/^[a-z]+$/.test(word)) {
    cmudictWords.add(word)
  }
}

const transitions = new Map()

for (const word of cmudictWords) {
  const paddedWord = `${"^".repeat(CONTEXT_SIZE)}${word}$`

  for (let index = CONTEXT_SIZE; index < paddedWord.length; index += 1) {
    const context = paddedWord.slice(index - CONTEXT_SIZE, index)
    const nextCharacter = paddedWord[index]
    const nextCharacters = transitions.get(context) ?? new Set()

    nextCharacters.add(nextCharacter)
    transitions.set(context, nextCharacters)
  }
}

const sortedTransitionEntries = [...transitions.entries()]
  .sort(([left], [right]) => left.localeCompare(right))
  .map(
    ([context, nextCharacters]) =>
      `  [${JSON.stringify(context)}, ${JSON.stringify([...nextCharacters].sort().join(""))}],`,
  )
  .join("\n")

const cmudictModule = `// Generated by scripts/derive-cmudict-model.mjs
// Source: ${CMUDICT_SOURCE_URL}

export const CMUDICT_SOURCE_URL = ${JSON.stringify(CMUDICT_SOURCE_URL)}
export const CMUDICT_CONTEXT_SIZE = ${CONTEXT_SIZE}
export const CMUDICT_WORD_COUNT = ${cmudictWords.size}

export const CMUDICT_TRANSITIONS = new Map<string, string>([
${sortedTransitionEntries}
])
`

const tempDir = await mkdtemp(join(tmpdir(), "wordloom-wordnet-"))

try {
  const wordnetArchivePath = join(tempDir, "WNdb-3.0.tar.gz")
  const wordnetResponse = await fetch(WORDNET_SOURCE_URL)

  if (!wordnetResponse.ok) {
    throw new Error(`Failed to download WordNet from ${WORDNET_SOURCE_URL}`)
  }

  await writeFile(wordnetArchivePath, Buffer.from(await wordnetResponse.arrayBuffer()))
  execFileSync("tar", ["-xzf", wordnetArchivePath, "-C", tempDir])

  const dictDir = join(tempDir, "dict")
  const partsOfSpeech = [
    { dataFile: "data.noun", indexFile: "index.noun", label: "noun" },
    { dataFile: "data.verb", indexFile: "index.verb", label: "verb" },
    { dataFile: "data.adj", indexFile: "index.adj", label: "adjective" },
    { dataFile: "data.adv", indexFile: "index.adv", label: "adverb" },
  ]
  const entriesByLemma = new Map()
  const offsetsByDataFile = new Map(partsOfSpeech.map(({ dataFile }) => [dataFile, new Set()]))

  for (const { dataFile, indexFile, label } of partsOfSpeech) {
    const rawIndex = await readFile(join(dictDir, indexFile), "utf8")

    for (const line of rawIndex.split(/\r?\n/)) {
      if (!line || line.startsWith("  ")) {
        continue
      }

      const fields = line.trim().split(/\s+/)
      const lemma = fields[0]

      if (!/^[a-z]+$/.test(lemma)) {
        continue
      }

      const pointerCount = Number.parseInt(fields[3] ?? "0", 10)
      const firstOffset = fields[6 + pointerCount]

      if (!firstOffset) {
        continue
      }

      offsetsByDataFile.get(dataFile)?.add(firstOffset)

      const entries = entriesByLemma.get(lemma) ?? []
      entries.push({ dataFile, label, offset: firstOffset })
      entriesByLemma.set(lemma, entries)
    }
  }

  const glossesByDataFile = new Map()

  for (const { dataFile } of partsOfSpeech) {
    const offsets = offsetsByDataFile.get(dataFile) ?? new Set()
    const rawData = await readFile(join(dictDir, dataFile), "utf8")
    const glosses = new Map()

    for (const line of rawData.split(/\r?\n/)) {
      if (!line || line.startsWith("  ")) {
        continue
      }

      const separatorIndex = line.indexOf("|")

      if (separatorIndex === -1) {
        continue
      }

      const offset = line.slice(0, separatorIndex).trim().split(/\s+/, 1)[0]

      if (!offsets.has(offset)) {
        continue
      }

      const gloss = line
        .slice(separatorIndex + 1)
        .trim()
        .replace(/\s+/g, " ")
      const definition = definitionOnly(gloss).replaceAll("|", "/")

      if (!definition) {
        continue
      }

      glosses.set(offset, definition)
    }

    glossesByDataFile.set(dataFile, glosses)
  }

  const definitions = new Map()

  for (const [lemma, entries] of entriesByLemma.entries()) {
    const meanings = entries
      .map(({ dataFile, label, offset }) => {
        const gloss = glossesByDataFile.get(dataFile)?.get(offset)

        if (!gloss) {
          return null
        }

        return `${label}: ${gloss}`
      })
      .filter(Boolean)

    if (!meanings.length) {
      continue
    }

    definitions.set(lemma, meanings.join("; "))
  }

  const sortedDefinitionEntries = [...definitions.entries()]
    .sort(([left], [right]) => left.localeCompare(right))
    .map(([lemma, meaning]) => `  [${JSON.stringify(lemma)}, ${JSON.stringify(meaning)}],`)
    .join("\n")

  const wordnetModule = `// Generated by scripts/derive-cmudict-model.mjs
// Source: ${WORDNET_SOURCE_URL}

export const WORDNET_SOURCE_URL = ${JSON.stringify(WORDNET_SOURCE_URL)}
export const WORDNET_DEFINITION_COUNT = ${definitions.size}

export const WORDNET_DEFINITIONS = new Map<string, string>([
${sortedDefinitionEntries}
])
`

  await mkdir(dirname(CMUDICT_OUTPUT_PATH), { recursive: true })
  await writeFile(CMUDICT_OUTPUT_PATH, cmudictModule)
  await writeFile(WORDNET_OUTPUT_PATH, wordnetModule)
} finally {
  await rm(tempDir, { force: true, recursive: true })
}
